void MPU6050_GetYaw(void)
{
	float gyro_z_dps;

	/* 偏航角只需要Z轴角速度，减少软件I2C读取时间。 */
	MPU6050_Data.GyroZ = MPU6050_ReadGyroZ();

	/* GyroZ量程为±2000°/s；车辆顺时针右转定义为偏航角正方向。 */
	gyro_z_dps = ((float)MPU6050_Data.GyroZ - s_gyro_z_bias)
				 * (2000.0f / 32768.0f) * MPU6050_YAW_CLOCKWISE_SIGN;
	MPU6050_GyroZDps = gyro_z_dps;
	AngleGyro.GyroZ = AngleFilter.Yaw
					 + gyro_z_dps * MPU6050_SAMPLE_DT_S;
	AngleFilter.Yaw = AngleGyro.GyroZ;
}

static int16_t MPU6050_ReadGyroZ(void)
{
	uint8_t DataH = MPU6050_ReadReg(MPU6050_GYRO_ZOUT_H);
	uint8_t DataL = MPU6050_ReadReg(MPU6050_GYRO_ZOUT_L);

	return (int16_t)(((uint16_t)DataH << 8) | DataL);
}

static void MPU6050_CalibrateGyroZ(void)
{
	int32_t gyro_sum = 0;
	uint16_t i;

	for (i = 0U; i < MPU6050_GYRO_CALIBRATION_SAMPLES; i++)
	{
		gyro_sum += MPU6050_ReadGyroZ();
		Delay_Ms(MPU6050_SAMPLE_PERIOD_MS);
	}

	s_gyro_z_bias = (float)gyro_sum / (float)MPU6050_GYRO_CALIBRATION_SAMPLES;
}

static void MPU6050_TimerInit(void)
{
	TIM_TimeBaseInitTypeDef TIM_TimeBaseInitStructure = {0};
	NVIC_InitTypeDef NVIC_InitStructure = {0};

	RCC_APB1PeriphClockCmd(RCC_APB1Periph_TIM4, ENABLE);

	/* TIM4先分频到10 kHz，再计数100次，产生10 ms更新中断。 */
	TIM_TimeBaseInitStructure.TIM_Prescaler =
		(uint16_t)(SystemCoreClock / MPU6050_TIMER_COUNTER_HZ - 1U);
	TIM_TimeBaseInitStructure.TIM_CounterMode = TIM_CounterMode_Up;
	TIM_TimeBaseInitStructure.TIM_Period =
		(uint16_t)(MPU6050_TIMER_COUNTER_HZ * MPU6050_SAMPLE_PERIOD_MS / 1000U - 1U);
	TIM_TimeBaseInitStructure.TIM_ClockDivision = TIM_CKD_DIV1;
	TIM_TimeBaseInitStructure.TIM_RepetitionCounter = 0;
	TIM_TimeBaseInit(TIM4, &TIM_TimeBaseInitStructure);

	TIM_ClearITPendingBit(TIM4, TIM_IT_Update);
	TIM_ITConfig(TIM4, TIM_IT_Update, ENABLE);

	NVIC_InitStructure.NVIC_IRQChannel = TIM4_IRQn;
	NVIC_InitStructure.NVIC_IRQChannelPreemptionPriority = 2;
	NVIC_InitStructure.NVIC_IRQChannelSubPriority = 0;
	NVIC_InitStructure.NVIC_IRQChannelCmd = ENABLE;
	NVIC_Init(&NVIC_InitStructure);

	TIM_Cmd(TIM4, ENABLE);
}

void TIM4_IRQHandler(void) __attribute__((interrupt("WCH-Interrupt-fast")));
void TIM4_IRQHandler(void)
{
	if (TIM_GetITStatus(TIM4, TIM_IT_Update) != RESET)
	{
		TIM_ClearITPendingBit(TIM4, TIM_IT_Update);
		Button_Tick10ms();
		MPU6050_GetYaw();
		MPU6050_YawSampleCount++;
	}
}
